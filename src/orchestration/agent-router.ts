/**
 * Маршрутизация задач по специализации и FIFO-очередь выполнения
 * (`src/orchestration/agent-router.ts`).
 *
 * Модуль кодирует две связанные дисциплины слоя оркестрации:
 *
 * 1. {@link route} — чистая функция принятия решения о маршрутизации Task в
 *    конкретной технической области (`area`) по пулу профильных
 *    Specialist_Agent и факту занятости исполнителя:
 *    - нет агента с меткой `area` ⇒ `blocked` с указанием несопоставленной
 *      области (Req 5.6);
 *    - есть свободный совпадающий агент И исполнитель не занят ⇒ `assigned`
 *      с идентификатором этого агента (Req 5.3, 5.4);
 *    - совпадающие агенты есть, но все заняты, либо исполнитель занят ⇒
 *      `queued` (Req 5.7, 7.6).
 *
 * 2. {@link TaskQueue} — глобальная FIFO-очередь, поддерживающая инвариант
 *    «не более одной Task в работе» (Req 7.4, 7.5). `enqueue` добавляет в хвост
 *    (Req 7.6), `completeCurrent` снимает текущую и продвигает голову очереди
 *    (Req 7.7). Порядок `current` строго совпадает с порядком прибытия
 *    (FIFO, Req 7.6, 7.7).
 *
 * Тип {@link Task} переиспользуется из `task-validator`, чтобы избежать
 * дублирующих определений доменной модели Task.
 *
 * См. design.md → "Components and Interfaces" → "Слой оркестрации —
 * src/orchestration/" → `agent-router.ts`, а также "Архитектура" →
 * "Дисциплина выполнения задач (FIFO + маршрутизация)" и "Инварианты данных" →
 * "TaskQueue".
 *
 * Validates: Requirements 5.1, 5.2, 5.3, 5.4, 5.6, 5.7, 7.4, 7.5, 7.6, 7.7
 */

import type { Task } from './task-validator';

/**
 * Профильный агент-исполнитель (Specialist_Agent).
 *
 * @property id - уникальный идентификатор агента, возвращаемый при назначении.
 * @property specialization - ровно одна метка технической области, по которой
 *   выполняется маршрутизация (Req 5.1). Допускается несколько агентов с
 *   одинаковой меткой (Req 5.2).
 * @property busy - признак занятости: `true`, если агент в данный момент
 *   выполняет другую Task и не может принять новую (Req 5.4, 5.7).
 */
export interface SpecialistAgent {
  id: string;
  specialization: string;
  busy: boolean;
}

/**
 * Результат решения о маршрутизации Task.
 *
 * - `assigned` — Task назначена свободному совпадающему агенту `agentId`
 *   (Req 5.3, 5.4);
 * - `queued` — совпадающие агенты есть, но все заняты, либо исполнитель занят;
 *   Task ставится в очередь (Req 5.7, 7.6);
 * - `blocked` — нет ни одного агента с меткой `area`; указывается
 *   несопоставленная область `unmatchedArea` (Req 5.6).
 */
export type RouteOutcome =
  | { kind: 'assigned'; agentId: string }
  | { kind: 'queued' }
  | { kind: 'blocked'; unmatchedArea: string };

/**
 * Принимает решение о маршрутизации Task в технической области `area`.
 *
 * Логика (Req 5.3, 5.4, 5.6, 5.7):
 * 1. Если ни один Specialist_Agent не имеет метки `specialization === area`,
 *    возвращается `blocked` с `unmatchedArea === area` — Orchestrator сообщает
 *    о несопоставленной области и не создаёт агента/задачу сам (Req 5.6).
 * 2. Иначе, если существует свободный (`busy === false`) совпадающий агент И
 *    исполнитель не занят (`executorBusy === false`), возвращается `assigned` с
 *    идентификатором этого агента; выбирается первый свободный совпадающий
 *    агент в порядке перечисления пула (Req 5.3, 5.4).
 * 3. Во всех остальных случаях (все совпадающие агенты заняты либо исполнитель
 *    занят) возвращается `queued` — Task ставится в хвост очереди до
 *    освобождения хотя бы одного совпадающего агента (Req 5.7, 7.6).
 *
 * Функция чистая: входной пул `agents` не мутируется.
 *
 * @param area - метка технической области Task.
 * @param agents - пул доступных Specialist_Agent.
 * @param executorBusy - выполняется ли в данный момент другая Task (дисциплина
 *   «одна за раз», Req 7.4, 7.5).
 * @returns решение о маршрутизации {@link RouteOutcome}.
 */
export function route(
  area: string,
  agents: SpecialistAgent[],
  executorBusy: boolean
): RouteOutcome {
  const matching = agents.filter((agent) => agent.specialization === area);

  // Нет ни одного агента с меткой area — блокировка (Req 5.6).
  if (matching.length === 0) {
    return { kind: 'blocked', unmatchedArea: area };
  }

  // Свободный совпадающий агент при свободном исполнителе — назначение (Req 5.3, 5.4).
  const freeAgent = matching.find((agent) => !agent.busy);
  if (freeAgent !== undefined && !executorBusy) {
    return { kind: 'assigned', agentId: freeAgent.id };
  }

  // Совпадающие агенты есть, но все заняты, либо исполнитель занят — очередь (Req 5.7, 7.6).
  return { kind: 'queued' };
}

/**
 * Глобальная FIFO-очередь выполнения Task с инвариантом «не более одной задачи
 * в работе» (Req 7.4, 7.5).
 *
 * Семантика:
 * - {@link enqueue} — если в работе нет текущей Task, поступившая Task сразу
 *   становится `current`; иначе она добавляется в хвост очереди ожидания
 *   (Req 7.6).
 * - {@link completeCurrent} — снимает текущую Task; если очередь ожидания
 *   непуста, голова очереди становится новой `current` (Req 7.7).
 * - {@link current} — текущая выполняемая Task или `null`; не более одной в
 *   любой момент (Req 7.4, 7.5).
 * - {@link pending} — задачи, ожидающие выполнения, в порядке прибытия (FIFO).
 *
 * Порядок, в котором задачи становятся `current`, строго совпадает с порядком
 * их поступления через `enqueue` (FIFO, Req 7.6, 7.7).
 */
export class TaskQueue {
  /** Текущая выполняемая Task; не более одной (Req 7.4, 7.5). */
  private _current: Task | null = null;

  /** Очередь ожидающих Task в порядке прибытия (FIFO, Req 7.6). */
  private readonly _pending: Task[] = [];

  /**
   * Помещает Task на выполнение или в хвост очереди ожидания.
   *
   * Если в данный момент нет текущей Task, поступившая Task немедленно
   * становится `current` (исполнитель свободен). Иначе Task добавляется в хвост
   * очереди ожидания и будет взята в работу после завершения предшествующих
   * (Req 7.6).
   *
   * @param task - поступившая на выполнение Task.
   */
  enqueue(task: Task): void {
    if (this._current === null) {
      this._current = task;
    } else {
      this._pending.push(task);
    }
  }

  /**
   * Завершает текущую Task и продвигает очередь.
   *
   * Снимает текущую Task; если очередь ожидания непуста, голова очереди
   * (первая поступившая) становится новой `current` (Req 7.7). Если очередь
   * пуста, `current` становится `null` (исполнитель простаивает).
   *
   * Вызов при отсутствии текущей Task безопасен и оставляет состояние
   * неизменным.
   */
  completeCurrent(): void {
    if (this._current === null) {
      return;
    }
    this._current = this._pending.shift() ?? null;
  }

  /**
   * Текущая выполняемая Task или `null`, если исполнитель простаивает.
   * В любой момент времени — не более одной (Req 7.4, 7.5).
   */
  get current(): Task | null {
    return this._current;
  }

  /**
   * Задачи, ожидающие выполнения, в порядке прибытия (FIFO, Req 7.6).
   * Возвращается доступная только для чтения проекция внутренней очереди.
   */
  get pending(): readonly Task[] {
    return this._pending;
  }
}
